public class RelationExp {
    public static void main (String argv[]) {
        System.out.println("3 > 2 = " + (3 > 2));
        System.out.println("3 >= 2 = " + (3 >= 2));
        System.out.println("2 >= 2 = " + (2 >= 2));
        System.out.println("3 != 2 = " + (3 != 2));
        System.out.println("3 < 2 = " + (3 < 2));
        System.out.println("3 == 2 = " + (3 == 2));
        System.out.println("2 != 2 = " + (2 != 2));
    }
}